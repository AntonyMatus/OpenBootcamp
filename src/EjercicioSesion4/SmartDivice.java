package EjercicioSesion4;

public class SmartDivice {

    //1. atributos
    public String marca;
    public String modelo;
    public int precio;
    public boolean wifi;

    //2. contructores

    public SmartDivice(){}

    public SmartDivice(String marca, String modelo, int precio, boolean wifi) {
        this.marca = marca;
        this.modelo = modelo;
        this.precio = precio;
        this.wifi = wifi;
    }

//3. metodos
}
